
public class Operador {
    public float soma( Float numA, Float numB){
        Float resultado;
        resultado = numA+numB;
        return resultado;
    }

    public float subtração(Float numA, Float numB){
        Float resultado;
        resultado = numA-numB;
        return resultado;
    }

    public float multipica (Float numA, Float numB){
        Float resultado;
        resultado = numA*numB;
        return resultado;
    }

    public float divisao (Float numA, Float numB){
        Float resultado;
        resultado = numA/numB;
        return resultado;
    }
}
